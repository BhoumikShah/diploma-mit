# Create a tuple
my_tuple = (12, 45, 9, 67, 3, 102)

# Find the minimum and maximum values in the tuple
minimum_value = min(my_tuple)
maximum_value = max(my_tuple)

# Display the results
print(f"Minimum value in the tuple: {minimum_value}")
print(f"Maximum value in the tuple: {maximum_value}")
